function renderBlank(text, state) {
  let cls = 'sc-blank';
  if (state === 'listening') cls += ' sc-blank-pulse';
  if (state === 'correct')   cls += ' sc-blank-correct';
  if (state === 'wrong')     cls += ' sc-blank-wrong';
  return text.replace(/___/, `<span class="${cls}" id="sc-blank-span">___</span>`);
}

function fillBlank(word, state) {
  const span = document.getElementById('sc-blank-span');
  if (!span) return;
  span.textContent = word;
  span.className   = 'sc-blank sc-blank-' + state;
}

function setBtnState(state) {
  const btn   = document.getElementById('sc-btn');
  const label = document.getElementById('sc-btn-label');
  btn.className = 'record-btn ' + state;
  if (state === 'idle')       { label.textContent = 'Start Listening'; btn.disabled = false; }
  else if (state === 'active'){ label.textContent = 'Listening…';      btn.disabled = false; }
  else if (state === 'busy')  { label.textContent = 'Processing…';     btn.disabled = true;  }
}

// ── VAD & recording ──
async function toggleSC() {
  if (scProcessing) return;
  if (scListening) { stopSC(); return; }
  await startSC();
}

async function startSC() {
  try {
    scStream    = await navigator.mediaDevices.getUserMedia({audio:true});
    scAudioCtx  = new AudioContext();
    scSampleRate= scAudioCtx.sampleRate;
    const src   = scAudioCtx.createMediaStreamSource(scStream);
    scProcessor = scAudioCtx.createScriptProcessor(2048, 1, 1);
    src.connect(scProcessor);
    scProcessor.connect(scAudioCtx.destination);
  } catch(e) {
    document.getElementById('sc-status').textContent = 'Mic access denied.';
    return;
  }
  scPcmChunks  = [];
  scListening  = true;
  scSpeechStart= 0;
  if (scSilenceTimer) { clearTimeout(scSilenceTimer); scSilenceTimer = null; }

  const s = currentSentence();
  if (s) document.getElementById('sc-sentence').innerHTML = renderBlank(s.text, 'listening');

  setBtnState('active');
  document.getElementById('sc-status').textContent = 'Say the missing word…';

  scProcessor.onaudioprocess = function(e) {
    if (!scListening) return;
    const data = e.inputBuffer.getChannelData(0);
    let rms = 0;
    for (let i = 0; i < data.length; i++) rms += data[i] * data[i];
    rms = Math.sqrt(rms / data.length);
    if (rms >= RMS_THRESH) {
      if (!scSpeechStart) scSpeechStart = Date.now();
      clearTimeout(scSilenceTimer);
      scSilenceTimer = null;
      scPcmChunks.push(new Float32Array(data));
    } else if (scSpeechStart) {
      if (!scSilenceTimer) {
        scSilenceTimer = setTimeout(() => {
          if (Date.now() - scSpeechStart >= MIN_SPEECH) {
            const dur = Date.now() - scSpeechStart;
            const pcm = mergePCM(scPcmChunks);
            stopSC();
            sendClip(pcm, scSampleRate, dur);
          } else {
            scPcmChunks  = [];
            scSpeechStart= 0;
          }
        }, SILENCE_MS);
      }
    }
  };
}

function stopSC() {
  scListening = false;
  clearTimeout(scSilenceTimer);
  scSilenceTimer = null;
  if (scProcessor) { try { scProcessor.disconnect(); } catch(e){} scProcessor = null; }
  if (scStream)    { scStream.getTracks().forEach(t => t.stop()); scStream = null; }
  if (scAudioCtx)  { try { scAudioCtx.close(); } catch(e){} scAudioCtx = null; }
  if (!scProcessing) setBtnState('idle');
}

function mergePCM(chunks) {
  const total = chunks.reduce((n, c) => n + c.length, 0);
  const out   = new Float32Array(total);
  let offset  = 0;
  for (const c of chunks) { out.set(c, offset); offset += c.length; }
  return out;
}

function pcmToWav(pcm, sr) {
  const buf    = new ArrayBuffer(44 + pcm.length * 2);
  const view   = new DataView(buf);
  const write  = (o, s) => { for (let i = 0; i < s.length; i++) view.setUint8(o + i, s.charCodeAt(i)); };
  write(0,  'RIFF'); view.setUint32(4,  36 + pcm.length * 2, true);
  write(8,  'WAVE'); write(12, 'fmt '); view.setUint32(16, 16, true);
  view.setUint16(20, 1, true); view.setUint16(22, 1, true);
  view.setUint32(24, sr, true); view.setUint32(28, sr * 2, true);
  view.setUint16(32, 2, true); view.setUint16(34, 16, true);
  write(36, 'data'); view.setUint32(40, pcm.length * 2, true);
  let o = 44;
  for (let i = 0; i < pcm.length; i++, o += 2) {
    const s = Math.max(-1, Math.min(1, pcm[i]));
    view.setInt16(o, s < 0 ? s * 0x8000 : s * 0x7FFF, true);
  }
  return buf;
}

// ── Send clip ──
async function sendClip(pcm, sr, speechMs) {
  scProcessing = true;
  setBtnState('busy');
  document.getElementById('sc-status').textContent = 'Processing…';

  const sentence = currentSentence();
  if (!sentence) { scProcessing = false; setBtnState('idle'); return; }

  const t0  = Date.now();
  const wav = pcmToWav(pcm, sr);
  const fd  = new FormData();
  fd.append('file',        new Blob([wav], {type:'audio/wav'}), 'clip.wav');
  fd.append('sentence_id', sentence.id);

  let result;
  try {
    const resp = await fetch(`${SC_SERVER}/sentence-completion/check`, {
      method: 'POST', body: fd
    });
    result = await resp.json();
  } catch(e) {
    document.getElementById('sc-status').textContent = 'Server error.';
    scProcessing = false;
    setBtnState('idle');
    return;
  }

  const ms = Date.now() - t0;
  showResult(result, sentence, speechMs, ms);
}

// ── Closeness labels ──
const CLOSENESS_INFO = {
  exact:   { icon: '🎉', label: 'Exact match',          cls: 'sc-close-exact' },
  synonym: { icon: '✨', label: 'Accepted synonym',      cls: 'sc-close-synonym' },
  close:   { icon: '🤏', label: 'Close, but not quite',  cls: 'sc-close-close' },
  wrong:   { icon: '💥', label: 'Incorrect',             cls: 'sc-close-wrong' },
};

// ── Sound effects (generated via Web Audio API — no external files) ──
function playCorrectSound() {
  try {
    const ctx = new AudioContext();
    // Cheerful ascending arpeggio
    [0, 0.12, 0.24, 0.4].forEach((t, i) => {
      const osc = ctx.createOscillator();
      const gain = ctx.createGain();
      osc.type = 'sine';
      osc.frequency.value = [523, 659, 784, 1047][i]; // C5, E5, G5, C6
      gain.gain.setValueAtTime(0.15, ctx.currentTime + t);
      gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + t + 0.3);
      osc.connect(gain).connect(ctx.destination);
      osc.start(ctx.currentTime + t);
      osc.stop(ctx.currentTime + t + 0.35);
    });
    setTimeout(() => ctx.close(), 1500);
  } catch(e) {}
}

function playWrongSound() {
  try {
    const ctx = new AudioContext();
    // Descending "buzz" — two low tones
    [0, 0.15].forEach((t, i) => {
      const osc = ctx.createOscillator();
      const gain = ctx.createGain();
      osc.type = 'triangle';
      osc.frequency.value = [220, 165][i]; // A3, E3
      gain.gain.setValueAtTime(0.12, ctx.currentTime + t);
      gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + t + 0.35);
      osc.connect(gain).connect(ctx.destination);
      osc.start(ctx.currentTime + t);
      osc.stop(ctx.currentTime + t + 0.4);
    });
    setTimeout(() => ctx.close(), 1500);
  } catch(e) {}
}

function playCloseSound() {
  try {
    const ctx = new AudioContext();
    // Wavering "almost" tone
    const osc = ctx.createOscillator();
    const gain = ctx.createGain();
    osc.type = 'sine';
    osc.frequency.setValueAtTime(440, ctx.currentTime);
    osc.frequency.linearRampToValueAtTime(415, ctx.currentTime + 0.2);
    osc.frequency.linearRampToValueAtTime(440, ctx.currentTime + 0.4);
    gain.gain.setValueAtTime(0.12, ctx.currentTime);
    gain.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + 0.5);
    osc.connect(gain).connect(ctx.destination);
    osc.start(); osc.stop(ctx.currentTime + 0.55);
    setTimeout(() => ctx.close(), 1500);
  } catch(e) {}
}

// ── Particle system for celebrations ──
let _particleAnim = null;
function launchParticles(type) {
  const canvas = document.getElementById('sc-particles');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');
  const rect = canvas.parentElement.getBoundingClientRect();
  canvas.width = rect.width;
  canvas.height = rect.height;

  const colors = type === 'correct'
    ? ['#5ddb5d','#88ff88','#44cc44','#ffffff','#aaffaa','#ffdd44']
    : type === 'close'
    ? ['#f0b840','#ffdd77','#cc9922','#ffffff','#ffeeaa']
    : ['#f06060','#ff8888','#cc3333','#ffffff','#ffaaaa'];

  const particles = [];
  const count = type === 'correct' ? 60 : 25;
  for (let i = 0; i < count; i++) {
    particles.push({
      x: canvas.width * Math.random(),
      y: canvas.height + 10,
      vx: (Math.random() - 0.5) * 6,
      vy: -(3 + Math.random() * 6),
      size: 3 + Math.random() * 5,
      color: colors[Math.floor(Math.random() * colors.length)],
      life: 1.0,
      decay: 0.008 + Math.random() * 0.012,
      gravity: 0.08,
      shape: Math.random() > 0.5 ? 'circle' : 'star',
    });
  }

  if (_particleAnim) cancelAnimationFrame(_particleAnim);

  function draw() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    let alive = false;
    for (const p of particles) {
      if (p.life <= 0) continue;
      alive = true;
      p.x += p.vx;
      p.vy += p.gravity;
      p.y += p.vy;
      p.life -= p.decay;
      ctx.globalAlpha = Math.max(0, p.life);
      ctx.fillStyle = p.color;
      if (p.shape === 'circle') {
        ctx.beginPath();
        ctx.arc(p.x, p.y, p.size, 0, Math.PI * 2);
        ctx.fill();
      } else {
        drawStar(ctx, p.x, p.y, p.size);
      }
    }
    ctx.globalAlpha = 1;
    if (alive) _particleAnim = requestAnimationFrame(draw);
  }
  draw();
}

function drawStar(ctx, x, y, r) {
  ctx.beginPath();
  for (let i = 0; i < 5; i++) {
    const angle = (i * 4 * Math.PI) / 5 - Math.PI / 2;
    const method = i === 0 ? 'moveTo' : 'lineTo';
    ctx[method](x + r * Math.cos(angle), y + r * Math.sin(angle));
  }
  ctx.closePath();
  ctx.fill();
}

// ── Show result — FLASHY ──
function showResult(result, sentence, speechMs, serverMs) {
  const heard       = (result.transcript || '').trim();
  const correct     = result.correct;
  const canonical   = result.canonical;
  const closeness   = result.closeness || (correct ? 'exact' : 'wrong');
  const explanation = result.explanation || '';
  const info        = CLOSENESS_INFO[closeness] || CLOSENESS_INFO.wrong;

  // Fill blank
  const blankState = correct ? 'correct' : (closeness === 'close' ? 'close' : 'wrong');
  fillBlank(canonical, blankState);

  // Update scores
  if (correct) {
    scScore++;
    document.getElementById('sc-score').textContent = scScore;
  } else {
    scWrong++;
    document.getElementById('sc-wrong').textContent = scWrong;
  }

  // Show feedback zone
  const zone = document.getElementById('sc-feedback-zone');
  zone.style.display = 'block';
  zone.className = correct ? 'sc-zone-correct' : (closeness === 'close' ? 'sc-zone-close' : 'sc-zone-wrong');
  // Re-trigger entry animation
  zone.style.animation = 'none';
  zone.offsetHeight; // force reflow
  zone.style.animation = '';

  // Glow
  const glow = document.getElementById('sc-feedback-glow');
  glow.className = correct ? 'sc-glow-correct' : (closeness === 'close' ? 'sc-glow-close' : 'sc-glow-wrong');

  // Result banner
  const rDiv = document.getElementById('sc-result');
  rDiv.innerHTML = '';
  if (correct) {
    rDiv.innerHTML = `<span class="sc-result-icon-big">${info.icon}</span> Correct!`;
    if (closeness === 'synonym') {
      rDiv.innerHTML += ` <span class="sc-closeness-badge ${info.cls}">${info.label}</span>`;
    }
  } else {
    rDiv.innerHTML = `<span class="sc-result-icon-big">${info.icon}</span> The answer is "<strong>${canonical}</strong>"`;
    if (closeness === 'close') {
      rDiv.innerHTML += ` <span class="sc-closeness-badge ${info.cls}">${info.label}</span>`;
    }
  }

  // Shake for wrong
  if (!correct && closeness !== 'close') {
    zone.classList.add('sc-shake');
    setTimeout(() => zone.classList.remove('sc-shake'), 600);
  }

  // Explanation
  const explDiv = document.getElementById('sc-explanation');
  if (explanation) {
    explDiv.style.display = 'block';
    explDiv.innerHTML = `<span class="sc-expl-icon">💡</span> ${explanation}`;
  } else {
    explDiv.style.display = 'none';
  }

  // Heard
  const hDiv = document.getElementById('sc-heard');
  if (heard) {
    hDiv.style.display = 'block';
    hDiv.textContent = `🎤 Heard: "${heard}"`;
  } else {
    hDiv.style.display = 'none';
  }

  // 🎵 Play sound
  if (correct) {
    playCorrectSound();
    launchParticles('correct');
  } else if (closeness === 'close') {
    playCloseSound();
    launchParticles('close');
  } else {
    playWrongSound();
    launchParticles('wrong');
  }

  // Log entry
  scLog.push({
    num:       scLog.length + 1,
    sentence:  sentence.text,
    heard:     heard,
    canonical: canonical,
    correct:   correct,
    closeness: closeness,
    skipped:   false,
    ms:        speechMs,
    llm:       sentence.llm || false,
  });
  appendLogRow(scLog[scLog.length - 1]);

  document.getElementById('sc-status').textContent = '';
  scProcessing = false;
  setBtnState('idle');

  // Auto-advance (longer delay for richer explanations)
  const delay = correct ? 3500 : (explanation ? 6000 : 4000);
  setTimeout(() => {
    advanceSentence();
  }, delay);
}

function showSentence() {
  const s = currentSentence();
  if (!s) {
    document.getElementById('sc-sentence').textContent = 'No sentences available.';
    return;
  }
  const badge = document.getElementById('sc-cat-badge');
  badge.textContent = s.cat;
  badge.className = 'sc-cat-badge sc-cat-' + s.cat.toLowerCase().replace(/\s+/g, '-');
  document.getElementById('sc-sentence').innerHTML = renderBlank(s.text, 'idle');
  document.getElementById('sc-progress').textContent =
    `${(scQueueIdx % scQueue.length) + 1} / ${scQueue.length}`;
  // Hide feedback zone
  const zone = document.getElementById('sc-feedback-zone');
  if (zone) zone.style.display = 'none';
  document.getElementById('sc-status').textContent = '';
  setBtnState('idle');
}

async function advanceSentence() {
  scQueueIdx++;
  if (scQueueIdx >= scQueue.length) {
    // Fetch fresh batch from server (LLM generates new ones each time)
    await loadSentences();
    return;
  }
  showSentence();
}

// ── Skip ──
function scSkip() {
  if (scListening) stopSC();
  const sentence = currentSentence();
  if (!sentence) return;
  scLog.push({
    num:       scLog.length + 1,
    sentence:  sentence.text,
    heard:     '—',
    canonical: '(skip)',
    correct:   false,
    skipped:   true,
    ms:        0,
    llm:       sentence.llm || false,
  });
  appendLogRow(scLog[scLog.length - 1]);
  scSkipped++;
  document.getElementById('sc-skipped').textContent = scSkipped;
  advanceSentence();
}

// ── Log table ──
function appendLogRow(entry) {
  const tbody = document.getElementById('sc-log-body');
  document.getElementById('sc-log').style.display = 'block';

  const tr = document.createElement('tr');
  if (entry.skipped)  tr.className = 'sc-log-skip';
  else if (entry.correct)  tr.className = 'sc-log-ok';
  else tr.className = 'sc-log-err';

  const shortSentence = entry.sentence.replace(/___/, '___');
  const llmIcon = entry.llm ? ' <span class="sc-llm-icon" title="AI-generated">✦</span>' : '';
  const closeIcon = entry.closeness === 'synonym' ? '≈' : entry.closeness === 'close' ? '~' : '';
  const resIcon = entry.skipped ? '↷' : entry.correct ? '✓' : (entry.closeness === 'close' ? '~' : '✗');
  tr.innerHTML = `
    <td>${entry.num}</td>
    <td class="sc-log-sent" title="${entry.sentence}">${shortSentence}${llmIcon}</td>
    <td class="sc-log-heard">${entry.heard}</td>
    <td class="sc-log-answer">${entry.canonical}</td>
    <td class="sc-log-res">${resIcon}</td>
    <td class="sc-log-ms">${entry.ms > 0 ? entry.ms : '—'}</td>
  `;
  tbody.appendChild(tr);
}
