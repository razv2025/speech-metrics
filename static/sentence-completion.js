'use strict';

const SC_SERVER = '';  // relative — same origin
const SILENCE_MS   = 600;
const RMS_THRESH   = 0.012;
const MIN_SPEECH   = 150;

// ── State ──
let scAllSentences = [];   // current batch from server
let scQueue        = [];   // shuffled ids for current category
let scQueueIdx     = 0;
let scListening    = false;
let scProcessing   = false;
let scSpeechStart  = 0;
let scSilenceTimer = null;
let scAudioCtx     = null;
let scStream       = null;
let scProcessor    = null;
let scPcmChunks    = [];
let scSampleRate   = 16000;
let scScore        = 0;
let scWrong        = 0;
let scSkipped      = 0;
let scLog          = [];
let scLlmAvailable = false;
let scCategories   = [];

// ── Init ──
(async function init() {
  // Load categories from server
  try {
    const catResp = await fetch(`${SC_SERVER}/sentence-completion/categories`);
    const catData = await catResp.json();
    scLlmAvailable = catData.llm_available;
    scCategories = catData.categories || [];
    buildCategoryDropdown();
    updateLlmBadge();
  } catch(e) {
    console.warn('Could not load categories, using defaults');
  }

  await loadSentences();
  initUsername();
})();

function initUsername() {
  fetch(`${SC_SERVER}/me`, {credentials:'include'})
    .then(r => r.json())
    .then(d => {
      const el = document.getElementById('nav-user');
      if (el && d.username) el.textContent = d.username;
    }).catch(() => {});
}

// ── Category dropdown ──
function buildCategoryDropdown() {
  const select = document.getElementById('sc-category');
  if (!select || !scCategories.length) return;

  // Clear existing options except "All"
  select.innerHTML = '<option value="all">All</option>';
  for (const cat of scCategories) {
    const opt = document.createElement('option');
    opt.value = cat.key;
    opt.textContent = cat.label;
    select.appendChild(opt);
  }
}

function updateLlmBadge() {
  const badge = document.getElementById('sc-llm-badge');
  if (!badge) return;
  if (scLlmAvailable) {
    badge.style.display = 'inline-flex';
    badge.textContent = '✦ AI-Generated';
    badge.title = 'Sentences generated by local LLM (Qwen2.5-14B)';
  } else {
    badge.style.display = 'inline-flex';
    badge.textContent = '📋 Classic';
    badge.title = 'Using built-in sentence library';
  }
}

// ── Load sentences from server ──
async function loadSentences() {
  const category = currentCategory();
  try {
    const resp = await fetch(
      `${SC_SERVER}/sentence-completion/sentences?category=${encodeURIComponent(category)}&count=15`
    );
    scAllSentences = await resp.json();
  } catch(e) {
    document.getElementById('sc-status').textContent = 'Could not load sentences.';
    return;
  }

  // Check if any are LLM-generated
  const hasLlm = scAllSentences.some(s => s.llm);
  const badge = document.getElementById('sc-llm-badge');
  if (badge) {
    if (hasLlm) {
      badge.style.display = 'inline-flex';
      badge.textContent = '✦ AI-Generated';
      badge.className = 'sc-llm-badge sc-llm-on';
    } else {
      badge.style.display = 'inline-flex';
      badge.textContent = '📋 Classic';
      badge.className = 'sc-llm-badge sc-llm-off';
    }
  }

  buildQueue();
  showSentence();
}

// ── Queue management ──
function currentCategory() {
  const el = document.getElementById('sc-category');
  return el ? el.value : 'all';
}

function buildQueue() {
  // Fisher-Yates shuffle
  const ids = scAllSentences.map(s => s.id);
  for (let i = ids.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [ids[i], ids[j]] = [ids[j], ids[i]];
  }
  scQueue = ids;
  scQueueIdx = 0;
}

async function onSCCategoryChange() {
  stopSC();
  await loadSentences();
}

function currentSentence() {
  if (!scQueue.length) return null;
  const id = scQueue[scQueueIdx % scQueue.length];
  return scAllSentences.find(s => s.id === id) || null;
}

